import numpy as np

# Данные
x = np.array([2.0, 2.2, 2.4, 2.6, 2.8, 3.0])
y = np.array([0.30, 0.34, 0.38, 0.42, 0.44, 0.48])

# Аппроксимация квадратным многочленом (степень 2)
coeffs = np.polyfit(x, y, 2)
a, b, c = coeffs  # y = a*x^2 + b*x + c

print(f"Квадратичная аппроксимирующая функция: y = {a:.6f}*x^2 + {b:.6f}*x + {c:.6f}")
