package main

import "fmt"

func main() {

	var int1 int = 100                   //Создание переменной целочисленного типа
	var float1 float64 = 100.1           //Создание переменной вещественного типа
	var string1 string = "Пример строки" //Создание переменной строчного типа
	var bool1 bool = true                //Создание переменной булевого типа

	fmt.Println("Integer: ", int1)
	fmt.Println("Float64: ", float1)
	fmt.Println("String: ", string1)
	fmt.Println("Boolean: ", bool1)

}
