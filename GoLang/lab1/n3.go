package main

import "fmt"

func main() {

	a := 1000            //Создание переменной целочисленного типа
	b := 10.101          //Создание переменной вещественного типа
	c := "Пример строки" //Создание переменной строчного типа
	d := true            //Создание переменной булевого типа

	fmt.Println("a =", a)
	fmt.Println("b =", b)
	fmt.Println("c =", c)
	fmt.Println("d =", d)

}
